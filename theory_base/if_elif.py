name1 = 'Олег'
name2 = 'Иван'
name3 = 'Василиса'

"""БЛОК elif выполняет одно действие из первого попавшегося
истинного условия"""

if name1 == 'Олег':
    print('Первого ученика зовут Олег')
if name2 == 'Иван':
    print('Второго ученика зовут Иван')
if name3 == 'Василиса':
    print('Третьего ученика зовут Василиса')
